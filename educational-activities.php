                <h4>Educational Activities</h4>

                <p><b>Morning Report</b></p>
                <p>Weekly morning case-based discussion that is attended by residents and faculty from different subspecialties. This conference focuses on clinical and diagnostic reasoning, generating an appropriate differential diagnosis along with evidence-based management and treatment of the cases discussed. Residents also build the confidence and competence to present long cases in a systematic and informative fashion.</p>

                <p><b>Morbidity &amp; Mortality</b></p>
                <p>Monthly case-based discussion that is led by residents and a faculty mentor. This conference aims to identify areas of improvement in clinical scenarios, recognize the need for updated policies and guidelines in certain areas in order to prevent future errors and allow for continued collaboration for better patient care.</p> 

                <p><b>Evidence Based Medicine (EBM) Journal Club</b></p>
                <p>Monthly sessions that are led by residents and a faculty mentor. It aims to develop the ability of critically appraising the literature and understanding how the results of a study can be incorporated into clinical practice. It provides a vital set of skills to ensure a life-long professional practice that is up to date and evidence-based.</p>

                <p><b>Grand Rounds</b></p>
                <p>Weekly morning conference that is hospital-wide and attendance is open to faculty and staff members across all departments. Lectures are presented by hospital faculty and guest lecturers. Topics cover the latest advances in the field of paediatrics and research.</p>

                <p><b>Academic Half Day</b></p>
                <p>Weekly protected time comprised of educational activities in the form of didactic lectures, board review questions, interactive case based presentations, games, bedside teaching that cover core educational topics led by our faculty.</p>

                <p><b>Peds In Review</b></p>
                <p>Weekly discussion of a Peds in Review article that is attended by residents and facilitated by a faculty mentor. It provides a space to reflect on the latest guidelines and discuss topics and skills that are important in the daily practice of paediatrics.</p>

                <p><b>Simulation Sessions</b></p>
                <p>Residents spend two weeks in the state-of-the-art simulation center in the prestigious MBRU to develop skills in performing core procedures.</p>

                <p><b>Mock Codes</b></p>
                <p>Monthly mock code scenarios led by the Emergency Department faculty that allow residents, nurses and members of the medical team to work together to enhance communication and management in emergent medical situations.</p>

